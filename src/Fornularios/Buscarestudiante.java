    private void cargarEstudiantes(){
        DefaultListModel<String> model = new DefaultListModel<>();
        for(int i = 0; i < daoE.getListaEstudiante().size(); i++){
            model.addElement(daoE.getListaEstudiante().get(i).getNombre() + 
                    daoE.getListaEstudiante().get(i).getApellido());
            
        }
        JLEstudiantes.setModel(model);
    }
