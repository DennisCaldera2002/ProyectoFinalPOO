    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        // TODO add your handling code here:
        if(!idActivado){
            int idEst = JLEstudiantes.getSelectedIndex();
            String fecha = JOptionPane.showInputDialog(null, "Ingrese la nueva "
                    + "fecha de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            String mes = JOptionPane.showInputDialog(null, "Ingrese el nuevo "
                    + "mes de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            String year = JOptionPane.showInputDialog(null, "Ingrese el nuevo "
                    + "año de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            
            int IdE = daoA.getListaAusencia().get(idEst).getIdEstudiante();
            String clase = daoD.buscarDocente(IdDocente).getClaseImpartida();
            
            int IDM = daoA.getGreaterID();
            
            daoA.modificarAusencia(IDM + 1, year, mes, fecha, idEst, clase);
            
        }
        else{
            int idEst = Integer.parseInt(txtIntroducir.getText());
            String fecha = JOptionPane.showInputDialog(null, "Ingrese la nueva "
                    + "fecha de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            String mes = JOptionPane.showInputDialog(null, "Ingrese el nuevo "
                    + "mes de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            String year = JOptionPane.showInputDialog(null, "Ingrese el nuevo "
                    + "año de esta ausencia: ", 
                    "Ausencia", JOptionPane.INFORMATION_MESSAGE );
            
            int IdE = daoA.getListaAusencia().get(idEst).getIdEstudiante();
            String clase = daoD.buscarDocente(IdDocente).getClaseImpartida();
            
            daoA.modificarAusencia(0, year, mes, fecha, idEst, clase);
        }
        daoA.BDactualizar();
        idActivado = false;
    }//GEN-LAST:event_btnModificarActionPerformed

